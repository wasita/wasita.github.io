			<div class="flex-1">
				<h3 class="text-purple-400 font-semibold">
					I study how we make sense of other people's minds and build shared understanding through interaction.
				</h3>
				<p class="pt-4 text-left">
					Our subjective realities are inherently limited, shaped by our personal perspectives and incomplete access to the experiences of others.
					How do we overcome the limitations of our own perspective to understand the minds of others and the shared experiences we have with them?
				</p>
				<p class="pt-4 text-left">
					To bridge the gap between our subjective realities, I study how we infer mental states through communication behavior, coordinate on meaning despite different interpretative frameworks, and align our mental states with others to achieve shared goals.
					These processes are foundational not only for successful collaboration but also for building meaningful social connections.
				</p>
				<p class="pt-4 text-left">
					By developing interactive web-based paradigms that preserve experimental control while capturing real social dynamics, I investigate the cognitive mechanisms that support our ability to co-construct mental models of the world with others. I combine real-time multiplayer experiments with natural language processing (NLP) and computational cognitive modeling (primarily Bayesian inference) to understand how minds interact and coordinate in the face of uncertainty.
				</p>
