 <h2 class="text-fuchsia-400 dark:text-fuchsia-300 font-semibold text-lg lg:text-xl leading-relaxed">
 I study how people navigate uncertainty about other minds through interaction and conversation, and how the process of figuring each other out gives rise to social connection.
 </h2>
 <div class="text-base lg:text-lg leading-relaxed text-gray-700 dark:text-gray-300">
 <p class="pt-4 text-left">
 When we meet someone new, we have direct access only to our own thoughts, feelings, and experiences. Yet we routinely form impressions of others, discover what we have in common, and build genuine bonds, often remarkably quickly. How do we infer, from sparse evidence, what we likely have in common with someone? What makes some discovered commonalities feel meaningful while others feel inconsequential? And how does real interaction shape the inferences we draw about others' minds?
 </p>
 <p class="pt-4 text-left">
 My dissertation work approaches these questions through the lens of social connection formation. Using custom multiplayer web experiments that I design and build, I study how people generalize from specific commonalities to broader shared understanding, how shared experiences create connection through interpretive alignment, and how statistical reasoning about social structures shapes which commonalities feel meaningful. These experiments allow causal manipulation of what people discover about each other while preserving the dynamics of real social interaction, and I analyze the resulting data using natural language processing and computational cognitive modeling.
 </p>
 <p class="pt-4 text-left">
 Looking forward, I am extending this framework to how people construct and update models of other minds during joint action and planning, and what happens when this process breaks down. Social interaction requires ongoing inference about others' intentions, beliefs, and goals, and failures in this process may underlie difficulties in social functioning across a range of psychiatric conditions. By bridging social cognition, computational modeling, and clinical science, I hope to build a mechanistic account of how we come to feel, or fail to feel, truly understood by others.
 </p>
